
	
	public class Vertex {
	    final private HeapEdge id;
	    


	    public Vertex(HeapEdge id) {
	        this.id = id;
	       
	    }
	    public HeapEdge getId() {
	        return id;
	    }

	   

	  
	    
	    

	    @Override
	    public String toString() {
	        return ""+id;
	    }

	}


